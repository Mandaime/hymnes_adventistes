import json
import pprint

clean_data = {"data": []}
for data in datas:
    cleaned = {
        "number": "",
        "data": {},
        "refrain": "",
    }

    number = data["_name"].replace("Cantique", "")
    content = data["__text"].replace('"', "").replace("'", "").splitlines()
    refrain = []
    if "i" in data:
        refrain = data["i"]["__text"].replace('"', "").replace("'", "").splitlines()
    ref = []
    for index, line in enumerate(refrain):
        if line not in ["", " ", ", "]:
            cleanline = line.strip()
            ref.append(cleanline)
    cleaned["refrain"] = ref
    cleaned["number"] = number
    cleaned["title"] = titles[int(number) - 1]
    for index, line in enumerate(content):
        if line.strip().isnumeric():
            linedata = []
            for i in range(index + 1, len(content)):
                if content[i].strip().isnumeric():
                    break
                else:
                    if content[i] not in ["", " ", ", ", "", " "]:
                        linedata.append(content[i].strip())
            cleaned["data"][line.strip()] = linedata

    pprint.pprint(cleaned)
    clean_data["data"].append(cleaned)
with open("../assets/data/hymns-full.json", "w", encoding="utf-8") as f:
    json.dump(clean_data, f, ensure_ascii=False, indent=4)
